// Switch

let option = 2

switch (option) {
  case 1:
    console.log("Consultar pedido")
    console.log("Aguarde...")
    break
  case 2:
    console.log("Falar com atendente")
    break
  case 3:
    console.log("Cancelar pedido")
    break
  default:
    console.log("opção inválida")
}

// Comando break interrompe o switch.